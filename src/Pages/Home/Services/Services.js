import React, { useEffect, useState } from 'react';
import { Badge } from 'react-bootstrap';
import Service from '../Service/Service';
import './Services.css';

const Services = () => {
    const [services, setServices] = useState([])
    useEffect(() => {
        fetch('https://enigmatic-crag-45724.herokuapp.com/services')
            .then(res => res.json())
            .then(data => setServices(data));
    }, [])

    return (
        <div id="services">
            <h3><Badge bg="secondary">Pending Service Request</Badge></h3>
            <strong className="text-primary mt-5">Our Team will contact you</strong>
            <div className="service-container">
                {
                    services.map(service => <Service
                        key={service.id}
                        service={service}
                    ></Service>)
                }
            </div>
        </div>
    );
};

export default Services;